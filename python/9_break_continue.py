for i in range(50):
    print(i)    #print 0 to 49
# Break in python
for i in range(50):
    if i==25 :
        break     #break exit the loop right now when condition meets
    print(i)
# continue in python
for i in range(50):
    if i==25 :
        continue     #continue skips the iteration when condition meets
    print(i)